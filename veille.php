          <img src="images/htcvibe.PNG" class="rounded mx-auto d-block img-thumbnail" alt="casque disque">
        </div><br>
				<p>
					Ce qui différencie le HTC Vive des autres casques du marché, c'est l'ajout de deux émetteurs
					"Lighthouse" à placer de part et d'autre de la pièce où l'appareil est utilisé.<br>
					Ces émetteurs (laser
					et infrarouge) sont essentiels pour capter la présence du casque. Deux contrôleurs sans fil qui
					vont devenir les mains de l'utilisateur dans l'univers virtuel sont également fournis.
