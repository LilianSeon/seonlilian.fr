			<h4>APPLICATION ACTUELLE ET FUTUR :</h4><br>
			<p>
				En effet, les attraits de la réalité virtuelle sont nombreux : pouvoir recréer des situations
dangereuses sans prendre de risque, former des techniciens sur des machines normalement coûteuse
ou encore proposer de visualiser en 3D un projet d’architecture pour mieux s’y projeter. Pouvoir
allier le minimum de risque et la réduction des coûts explique que de nombreuses entreprises
exploitent cette technologie. L’exemple le plus connu est sous doute la formation des pilotes de
ligne, où les casques sont complétés par l’interface d’une cabine de pilotage.<br><br>
Dans le futur, la NASA a par exemple un projet en association avec Microsoft et son casque
HoloLens pour les astronautes. Une fois dans l’espace, ceux-ci pourront être aidé pour effectuer des
réparations assistées par un expert au sol avec des informations affichés directement dans leur
casque. L’éducation va pouvoir aussi se doter de nouvelles manière d’apprentissage avec des salles
de cours virtuelles où l’on pourra voyager à travers le temps et l’espace pour étudier une époque ou
des lieux.
