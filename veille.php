La réalité virtuelle (en anglais,
virtual reality
ou VR) est une technologie qui permet de
plonger une personne dans un monde artificiel créé numériquement.<br>  Elle ne doit pas être
confondue avec la
réalité augmentée
.
Il peut s'agir d'une reproduction du monde réel ou bien d'un
univers
totalement
imaginaire.<br> L'expérience est à la fois visuelle, auditive et, dans certains cas, haptique
avec la production d'un retour d'effets. Lorsque la personne est équipée
des interfaces adéquates, comme des gants ou des vêtements, elle peut alors éprouver
certaines sensations liées au toucher ou à certaines actions (coup, impact...).<br>
la Réalité augmenté peut être aussi utilisée pour l'aide aux déplacement au sol

        </p>
