        <h3 id="casque"><u>2.Casque VR / Histoire</u></h3><br>
        <p>
          Cette immersion se fait au moyen d'un casque de réalité virtuelle qui place un système d'affichage 3D stéréoscopique sur le
nez,devant les yeux.<br> Certains modèles sont équipés de capteurs qui détectent les mouvements de la tête pour permettre à l'utilisateur de
regarder autour de lui.<br> Les images sont alors recalculées en temps réel pour se
synchroniser avec la direction du regard.
        </p>
        <div id="imgCasque">
          <img src="images/casquevr.PNG" class="rounded mx-auto d-block img-thumbnail" alt="Casque de réalité virtuelle playstation">
        </div>
        <br><h3>Histoire :</h3><br>
        <p>
          Dans les années 1939 le View-Master est un dispositif permettant de visionner des images en 3D à <br>partir de 'Bobine' se présentant sous la forme d'un disque.
          <br>Dans les années 1950, le Sensorama construit par Morton Heilig est conçu pour permettre
          au spectateur du cours métrage de vivre une expérience d'immersion visuelle et auditive dans une réelle filmée au préalable.
        </p>
