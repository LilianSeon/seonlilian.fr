        <p>
          le HTC Vive est le casque de réalité virtuelle qui était le plus attendu. En effet HTC promet plus <br>
          qu'une expérience de VR classique, en position assise, l'appareil permet une immersion de tout
          le corps, grâce à la possibilité<br> de se déplacer réellement sur plusieurs mètres et la présence de
          deux contrôleurs à reconnaissance de mouvements.<br> Techniquement, le HTC Vive est pourtant assez standard
          : on retrouve deux écrans Oled un par œil affichant chacun 1080 x 1200px, <br>un champ de vision de 110 degrés et une vitesse de rafraîchissement de 90Hz.
        <br> Pour profiter pleinement de ce casque HTC préconise :<br>
- Intel Core i5-4590.<br>
-4 Go de RAM.<br>
-Nvidia GeForce GTX 970 ou AMD R9 290.<br>
-Sortie HDMI 1.3.<br>
-2 ports USB 3.0 et un port USB 2.0.<br>
-Windows 7 64 bit.<br>
        </p>
