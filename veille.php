				<p>
					La plus grand différence du Samsung Gear VR avec les autres casques VR c'est que celui-ci s'utilise
avec son smartphone en guise d'écrant. Contrairement à ses concurrents (
Homido, Zeiss VR One ...) qui sont compatible avec plusieurs marques de téléphone, le casque de Samsung est
compatible qu'avec les smartphones Samsung Galaxy S6, S6 edge et S6 edge+. Il se démarque aussi
par sa conception partagée avec le géniteur des casques VR de nouvelle génération, Oculus
VR.
Samsung profite donc de l'expertise de ce dernier, qui maîtrise déjà bien les nouvelles
technologies de réalité virtuelle grâce au travail sur son casque maison, l'Oculus Rift.<br>
La mise en place du smartphone se montre relativement simple, grâce à un système de pince
sur ressort qui vient fermement caler le smartphone dans son logement. De petits inserts
souples évitent d'endommager les écrans du Galaxy S6.
</p>
